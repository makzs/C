#include<stdio.h>

/*
 Escreva um programa que apresente na tela a seguinte saída, fazendo uso de estrutura de repetição 
 para imprimir as linha com 20 asteriscos e os números de 1 até 5:
*/

int main(){
	int i;
	
	for(i = 1;i <= 20; i++){
		printf("*");
		
	}
	printf("\n");
	
	printf("Numeros entre 1 e 5 \n");
	
	for(i = 1;i <= 20; i++){
		printf("*");
		
	}
	printf("\n");
	
	for(i = 1; i <= 5; i++){
		printf("%d \n", i);
	}
	printf("\n");
	
	for(i = 1;i <= 20; i++){
		printf("*");
		
	}
}
